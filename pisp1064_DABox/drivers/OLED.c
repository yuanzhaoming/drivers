/* page: 0-7
 * col : 0-127
 * 字符: 8x16象素
 */
void OLEDPutChar(int page, int col, char c)
{
    int i = 0;
    /* 得到字模 */
    const unsigned char *dots = oled_asc2_8x16[c - ' '];

    /* 发给OLED */
    OLEDSetPos(page, col);
    /* 发出8字节数据 */
    for (i = 0; i < 8; i++)
        OLEDWriteDat(dots[i]);

    OLEDSetPos(page+1, col);
    /* 发出8字节数据 */
    for (i = 0; i < 8; i++)
        OLEDWriteDat(dots[i+8]);

}


/*
* 	stm32硬件spi发送数据
*
*
*/
unsigned char hal_SPI_OLED_SendByte(unsigned char byte)
{
	/* Loop while DR register in not emplty */
	while (SPI_I2S_GetFlagStatus(SPI3, SPI_I2S_FLAG_TXE) == RESET);
	/* Send byte through the SPI1 peripheral */
	SPI_I2S_SendData(SPI3, byte);
	/* Wait to receive a byte */
	while (SPI_I2S_GetFlagStatus(SPI3, SPI_I2S_FLAG_RXNE) == RESET);
	/* Return the byte read from the SPI bus */
	return SPI_I2S_ReceiveData(SPI3);
}

/*
* 		读取OLED传输过来的32位的数据，采用轮询方式
*
*/
u32 SPI_OLED_ReadByte(void)
{
	unsigned int c_rcv1;
	unsigned int c_rcv2;
	unsigned int c_rcv3;
	unsigned int c_rcv4;

	SPI_OLED_CS_LOW();

	c_rcv1 = hal_SPI_OLED_SendByte(0xff); 
	c_rcv2 = hal_SPI_OLED_SendByte(0xff); 
	c_rcv3 = hal_SPI_OLED_SendByte(0xff); 
	c_rcv4 = hal_SPI_OLED_SendByte(0xff); 

	SPI_OLED_CS_HIGH();
  	return (c_rcv1 << 24) | (c_rcv2 << 16 ) | (c_rcv3 << 8) | c_rcv4;
}

